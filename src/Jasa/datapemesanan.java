    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        koneksi();
        loadData();
	int pesan = JOptionPane.showConfirmDialog(null, "Anda Yakin Ingin Menghapus Data"+ Id_Member +"?","Konfirmasi",JOptionPane.OK_CANCEL_OPTION);
	if (pesan == JOptionPane.OK_OPTION){
	try{
	Statement stat = (Statement) Db_Koneksi.getKoneksi().createStatement();
	String sql = "DELETE FROM tbl_pesanan WHERE Id_Member = '"+ Id_Member +"'";
	PreparedStatement p = (PreparedStatement) Db_Koneksi.getKoneksi().prepareStatement(sql);	
	p.executeUpdate();
	tabel1();
	riset();
	JOptionPane.showMessageDialog(null, "Delete Berhasil");
	}catch(SQLException err){
		JOptionPane.showMessageDialog(null, err.getMessage());
	}
       } 
    }//GEN-LAST:event_btnDeleteActionPerformed

