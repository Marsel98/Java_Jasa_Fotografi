    private void tabel1(){ 
    DefaultTableModel t= new DefaultTableModel();
    t.addColumn("Id_Member"); 
    t.addColumn("Nama");
    t.addColumn("Alamat");
    t.addColumn("Jenis Kelamin");
    t.addColumn("Email");
    t.addColumn("No Telpon");
    t.addColumn("Lokasi Acara");

    tbl_Pesanan.setModel(t); try{ res=stat.executeQuery("select * from tbl_Pesanan"); 
    while (res.next()) { 
    t.addRow(new Object[]{ 
    res.getString("Id_Member"),
    res.getString("Nama"), 
    res.getString("Alamat"), 
    res.getString("Jenis_Kelamin"), 
    res.getString("Email"), 
    res.getString("No_Telp"), 
    res.getString("Lokasi_Acara"), 
    }); 
    }
    }catch (Exception e) { 
    ; 
    }
    }
    
